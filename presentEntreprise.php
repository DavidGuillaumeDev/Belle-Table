                <h2 style="color: rgb(77, 85, 7);">Notre Histoire</h2>
                <p>
                Suite à une perte d’emploi pour l’un, un souhait de création pour l’autre, deux frères,
                passionnés des arts de la table, ont décidé de créer leur société il y a quelques années, en
                2010. Ils ont choisi la forme.
                </p>
Pierre Bartholi, l’initiateur du projet, âgé de 45 ans à la création de la société, était
commercial dans ce domaine depuis près de 20 ans. Une restructuration du groupe pour
lequel il travaillait a conduit à des licenciements fermes et des possibilités de départ
négociés. C’est cette solution que Pierre a choisie. Il décide alors d’investir la totalité de son
indemnité dans la création de sa société. <br>
Il possède 55% des parts sociales à lui seul. Son épouse, Martine Bartholi, accompagne son
mari dans l’aventure. Elle possédera 10% des parts sociales. <br>
Marc Bartholi, jeune frère du créateur, souhaitait depuis quelques années créer sa propre
entreprise. Il profite de l’opportunité pour rejoindre son frère dans cette formidable
aventure. Agé alors de 40 ans, il rejoint la société et obtient par son apport financier 25% des
parts sociales. <br>
C’est tout naturellement que Laurence, épouse de Marc, accompagne également son mari et
prendra possession des 10% de parts sociales restantes. <br>
Ce sont 100.000 euros au total qui ont permis de composer le capital de la société grâce aux
apports financiers des actionnaires. <br>
Pierre Bartholi souhaite que le nom de cette société soit représentatif de son activité.
Les deux frères et leurs épouses se sont accordés sur « BELLETABLE ». <br>
L’objet de cette société est la vente et la location de tous les matériels, la vaisselle, les
couverts et tous autres accessoires permettant de dresser une « Belle Table ». <br>
Le logo de la société est créé à partir d’un graphisme stylisé rappelant le luxe à la française. <br>
La société a démarré avec seulement 4 salariés : 2 commerciaux, 1 comptable, 1 préparateur
de commandes. Pierre et Marc ont travaillé sans percevoir de salaire la première année. <br>
Les commerciaux se sont répartis les missions de façon alternatives. L’un au magasin, l’autre
en démarchage auprès des professionnels et associations pendant une semaine. La semaine 
