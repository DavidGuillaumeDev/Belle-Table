suivante les rôles s’inversaient entre les commerciaux. Toutefois, chacun d’eux respectait
son secteur de démarchage, Paris intramuros pour l’un, périphérie pour l’autre. <br>
Le chiffre d’affaires de la première année était de 750 K€, avec un bénéfice avant impôts de
90 K€. <br>
Cette première année très encourageante a permis de développer la communication et
d’augmenter l’équipe pour passer à 10 salariés. <br>
La deuxième année a connu un vif succès. C’est un chiffre d’affaires de 1,8 M€ qui est
enregistré. Il permettra un bénéfice avant impôts de 150 K€. <br>
La troisième année d’activité, Pierre est devenu Directeur Général, Marc Directeur
Marketing, tous deux salariés. Les épouses des actionnaires ont intégré la société également,
aux postes de Responsable de magasin pour Martine, Responsable vente à distance et
locations pour Laurence. <br>
L’équipe a été de nouveau renforcée avec 6 embauches supplémentaires au cours de la
troisième année afin de faire face à l’augmentation d’activité. <br>
Aujourd’hui, la société continue de prospérer avec une progression à deux chiffres du CA
chaque année pour atteindre 3,8 M€ au dernier exercice. <br>
Le nombre de salariés a suivi cette évolution. Ce sont 40 salariés qui travaillent au sein de la
société BELLETABLE, 37 à temps complet, 3 à mi-temps. Parfois, pour faire face à la charge
supplémentaire, la société BELLETABLE fait appel à un ou plusieurs intérimaires. <br>
Les clients sont aujourd’hui très variés et nombreux. Tous types de professionnels faisant
appel à la restauration font appel de façon régulière à BELLETABLE. Il y a des restaurants qui
souhaitent compléter ou renouveler la vaisselle, les couverts, voir la totalité de leur salle de
restauration, mais aussi des traiteurs (achats et surtout locations), les organisateurs
d’évènements, les associations, comités d’entreprise, principalement pour la location. <br>
Depuis quelques mois, BELLETABLE propose également un service complet avec la location
incluant la livraison, la mise en place des tables, l’installation des divers matériels, le service
à table, lavage de la vaisselle et du linge. Aucune autre société ne propose un service aussi
complet et haut de gamme avec des articles de la table de luxe. <br>
Une étude de marché préalable montre que cette extension des services doit générer une
augmentation de l’activité conséquente, estimée entre 600 et 800 K€. <br>
La société BELLETABLE s’est équipée au fil du temps de matériels informatiques afin de
prendre en charge la majorité des activités de l’entreprise. <br>
